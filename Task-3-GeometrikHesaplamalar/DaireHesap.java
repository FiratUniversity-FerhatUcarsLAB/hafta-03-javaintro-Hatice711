public class DaireHesap {
    public static void main(String[] args) {
        double r = (double)5.5F;
        double pi = 3.14159;
        double alan = pi * r * r;
        double cevre = (double)2.0F * pi * r;
        System.out.println(" Yaricap : " + r);
        System.out.println(" Alan : " + alan);
        System.out.println(" CEVRE : " + cevre);
    }
}
